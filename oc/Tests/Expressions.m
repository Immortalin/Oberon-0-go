(* Expressions Test Module *)
MODULE Expressions;
    VAR v: INTEGER;
BEGIN
    (* Addition *)
    Write(5 + 3);
    v := 5 + 3;
    Write(v);
    (* Subtraction *)
    Write(5 - 3);
    v := 5 - 3;
    Write(v);
    (* Multiplication *)
    Write(5 * 3);
    v := 5 * 3;
    Write(v);
    (* Division *)
    Write(5 DIV 3);
    v := 5 DIV 3;
    Write(v)
END Expressions.